    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultMutableTreeNode selectedNode = (DefaultMutableTreeNode) jTree1.getLastSelectedPathComponent();

        if (selectedNode == null) // Keine node ausgewählt
        {
            JOptionPane.showOptionDialog(null, "Fehler: Sie müssen zuerst eine Bestellung auswählen!", "Bestellungs Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
        }
        else if (selectedNode.isLeaf())
        {
            if (selectedNode.getLevel() == 1)
            {
                JOptionPane.showOptionDialog(null, "Fehler: Diese Bestellung ist fehlerhaft und besitzt keine Bestellungspositionen. Bitte Kontaktieren Sie den System-Administrator.", "Bestellungs Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
            }
            else 
            {
                JOptionPane.showOptionDialog(null, "Fehler: Wählen Sie bitte die Bestellung und nicht die Bestellungsposition an!", "Bestellungs Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
            }
        }
        else {
            String nodeText = (String) selectedNode.getUserObject();
            int bid = Integer.valueOf(nodeText.substring(4, nodeText.indexOf("-")).trim());
            boolean result = kundeSteuerungBestellung.storniereBestellung(bid);
            if (result) {
                JOptionPane.showOptionDialog(null, "Erfolg: Ihre gewählte Bestellung wurde storniert!", "Bestellungs Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{"OK"}, "OK");
                setBestellungen();
            } else {
                JOptionPane.showOptionDialog(null, "Fehler: Die Bestellung konnte nicht storniert werden.", "Bestellungs Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

