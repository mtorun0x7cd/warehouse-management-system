    private void setBestellungen() {
        DefaultTreeModel model = (DefaultTreeModel) jTree1.getModel();
        DefaultMutableTreeNode root = (DefaultMutableTreeNode) model.getRoot();
        root.removeAllChildren();
        jTree1.setRootVisible(false);
        List<BestellungGrenz> bestellungen = kundeSteuerungBestellung.getBestellungListe(kunde.getKid());

        for (BestellungGrenz b : bestellungen) {
            String nodeText = "ID: " + b.getBid() + " - Status: " + b.getStatus().name() + " - Lieferadr.: " + b.getLieferadresse() + " - Rechnungsadr.: " + b.getRechnungsadresse() + " - Angelegt: " + b.getAngelegt() + " - Gesamtbrutto: " + b.getGesamtBrutto().toString() + " - Gesamtnetto: " + b.getGesamtNetto().toString();
            DefaultMutableTreeNode node = new DefaultMutableTreeNode(nodeText);
            for (BestellungspositionGrenz bpos : b.getBestellungspositionsList()) {
                String subNodeText = "BPOS ID: " + bpos.getBpid() + " - Anzahl: " + bpos.getAnzahl() + " - Produkt Name: " + bpos.getProdukt().getName() + " - Einzel-Nettopreis: " + bpos.getProdukt().getNettopreis();
                DefaultMutableTreeNode subNode = new DefaultMutableTreeNode(subNodeText);
                node.add(subNode);
            }
            root.add(node);
        }
        model.reload();
    }

