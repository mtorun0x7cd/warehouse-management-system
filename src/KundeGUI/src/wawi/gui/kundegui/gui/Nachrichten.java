    public void getMessages() {
        List<NachrichtGrenz> items = kundeSteuerungNachricht.nachrichtenAbrufen(kunde.getKid());
        DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
        tableModel.setRowCount(0); // Alle Zeilen "loeschen"

        for (NachrichtGrenz msg : items) {
            tableModel.addRow(
                    new Object[]{
                        msg.getNid(),
                        msg.getBetreff(),
                        msg.getStatus().name(),
                        msg.getNachricht(),
                        msg.getAngelegt().toString(),
                        msg.getTyp().name()
                    });
        }

        if (shown) {
            JOptionPane.showOptionDialog(null, "Ihre Nachrichten wurden abgerufen. Es wurden " + items.size() + " abgerufen.", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{"OK"}, "OK");
        }
    }

