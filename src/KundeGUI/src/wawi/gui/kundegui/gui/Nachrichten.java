    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
        int selectedRowIdx = jTable2.getSelectedRow();

        if (selectedRowIdx < 0) {
            JOptionPane.showOptionDialog(null, "Fehler: Sie haben keine Nachricht ausgewaehlt die geloescht werden soll!", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
        } else {
            int nid = (int) tableModel.getValueAt(selectedRowIdx, 0); // selectedRow Index, 0 ( = Nachrichten ID)
            boolean result = kundeSteuerungNachricht.loescheNachricht(nid);
            if (result) {
                JOptionPane.showOptionDialog(null, "Erfolg! Ihre Nachricht wurde gelöscht", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{"OK"}, "OK");
                tableModel.removeRow(selectedRowIdx);
            } else {
                JOptionPane.showOptionDialog(null, "Fehler! Ihre Nachricht konnte nicht gelöscht werden!", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

