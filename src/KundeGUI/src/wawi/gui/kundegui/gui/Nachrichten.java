    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) jTable2.getModel();
        int selectedRowIdx = jTable2.getSelectedRow();

        if (selectedRowIdx < 0) {
            JOptionPane.showOptionDialog(null, "Fehler: Sie haben keine Zeile ausgewaehlt die geloescht werden soll!", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
        } else {
            int nid = (int) tableModel.getValueAt(selectedRowIdx, 0); // selectedRow Index, 0 ( = Nachrichten ID)
            boolean result = kundeSteuerungNachricht.setNachrichtGelesen(nid);
            if (result) {
                JOptionPane.showOptionDialog(null, "Erfolg! Ihre ausgewählte Nachricht wurde als gelesen markiert", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.INFORMATION_MESSAGE, null, new Object[]{"OK"}, "OK");
                shown = false;
                getMessages();
                shown = true;
            } else {
                JOptionPane.showOptionDialog(null, "Fehler! Ihre Nachricht konnte nicht auf gelesen gesetzt werden! Merke: Sie können nur Nachrichten, die an Sie adressiert sind als gelesen markieren!", "Nachrichten Verwaltung", JOptionPane.PLAIN_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{"OK"}, "OK");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

